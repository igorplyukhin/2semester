#!/usr/bin/env python3
import re
import requests


def get_content(name):
    """
    Функция возвращает содержимое вики-страницы name из русской Википедии.
    В случае ошибки загрузки или отсутствия страницы возвращается None.
    """
    f = requests.get("https://en.wikipedia.org/w/"
                     "api.php?action=parse&page={}&prop=wikitext&formatversion=2&format=json".format(name)).json()
    if "error" in f:
        return None
    else:
        return f["parse"]["wikitext"]


